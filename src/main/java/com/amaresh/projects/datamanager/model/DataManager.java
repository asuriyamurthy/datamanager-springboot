package com.amaresh.projects.datamanager.model;

public class DataManager {

	private String total_credit;
	private String total_expenses;
	private String balance;
	
	public String getTotal_credit() {
		return total_credit;
	}
	public void setTotal_credit(String total_credit) {
		this.total_credit = total_credit;
	}
	public String getTotal_expenses() {
		return total_expenses;
	}
	public void setTotal_expenses(String total_expenses) {
		this.total_expenses = total_expenses;
	}
	public String getBalance() {
		return balance;
	}
	public void setBalance(String balance) {
		this.balance = balance;
	}
}